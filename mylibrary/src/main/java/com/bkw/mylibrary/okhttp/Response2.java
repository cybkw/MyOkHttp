package com.bkw.mylibrary.okhttp;

public class Response2 {

    /**
     * 请求码
     */
    private int code;

    /**
     * 响应体内容
     */
    private String body;


    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
